16. التعامل مع المعلومات الشخصية

16.1 الحفاظ على معلومات شخصية دقيقة
يستخدم موقع الويب تدابير مناسبة لضمان أن المعلومات الشخصية للعميل تظل دقيقة ومحدثة.

16.2 فترة تخزين المعلومات الشخصية
سيقوم موقع الويب بتخزين المعلومات الشخصية فقط للفترة اللازمة لتحقيق الغرض من الاستخدام ، وفي غضون فترة معقولة بعد انتهاء فترة التخزين المطلوبة ، يقوم بمسح المعلومات الشخصية أو إخفاء هويتها بأمان.


17. عمليات نقل المعلومات الشخصية إلى دول ثالثة
عندما يكون نقل المعلومات الشخصية ضروريًا لأداء عقد بين العميل والموقع الإلكتروني والتطبيق والخدمات ذات الصلة أو تنفيذ التدابير التعاقدية المسبقة المتخذة بناءً على طلب العميل ، فقد نقوم بنقل المعلومات الشخصية التي تم الحصول عليها داخل الاتحاد الأوروبي إلى بلد ثالث. في حالة النقل إلى بلد يفتقر إلى الحماية الكافية ، سيتخذ الموقع التدابير اللازمة لضمان حماية البيانات التي تلبي معايير القانون العام لحماية البيانات (GDPR).
18. تأمين إدارة المعلومات الشخصية

6.1 الامتثال للتشريعات واللوائح والمبادئ التوجيهية ذات الصلة
يتوافق موقع الويب مع اللائحة العامة لحماية البيانات (GDPR) بالإضافة إلى القوانين واللوائح والمبادئ التوجيهية الصناعية الأخرى ذات الصلة.

6.2 الإجراءات الأمنية
يبذل موقع الويب قصارى جهده لحماية معلوماتك الشخصية من خلال تدابير وقائية وأمنية للحماية من الوصول غير المصرح به ، والخسارة ، والتدمير ، والعبث ، والتسريبات.



