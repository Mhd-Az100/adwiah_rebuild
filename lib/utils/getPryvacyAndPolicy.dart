15. توفير المعلومات الشخصية لأطراف ثالثة والاستخدام المشترك لها

عند استخدام معلوماتك الشخصية ، قد يقوم موقع الويب بالتعاقد من الباطن على معالجة هذه المعلومات أو تخزينها إلى طرف ثالث إلى حد الاستخدام المشروع.
